package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            int n = 1;
            while (n <= 10) {
                System.out.print("Q");
                n++;
            }
            System.out.println();
            i++;
        }
    }
}